#include <stdlib.h>
#include <stdio.h>
/**
*op_add - adds8
*/
int op_add(int a, int b)
{
return (a + b);
}
/**
*op_sub - subtracts
*/
int op_sub(int a, int b)
{
return (a - b);
}
/**
*op_mul - multiplys
*/
int op_mul(int a, int b)
{
return (a * b);
}
/**
*op_div - divides
*/
int op_div(int a, int b)
{
return (a / b);
}
/**
*op_mod - modulus
*/
int op_mod(int a, int b)
{
return (a % b);
}
